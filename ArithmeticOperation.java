package PracticeWallah;

public class ArithmeticOperation {

    public static void main(String[] args) {
        float x = 10;
        float y = 7;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);

    }
}